//==============================================================================
// ■ Crypt (crypt.js)
//------------------------------------------------------------------------------
//     Crypting utility functions.
//==============================================================================
const { hash, compare } = require("bcryptjs");

//------------------------------------------------------------------------------
// ► Exports
//------------------------------------------------------------------------------
module.exports = { encrypt, check };

//------------------------------------------------------------------------------
// ● Encrypt
//------------------------------------------------------------------------------
async function encrypt(text) {
  return hash(text, 12);
}

//------------------------------------------------------------------------------
// ● Check
//------------------------------------------------------------------------------
async function check(text, hash) {
  return compare(text, hash);
}
